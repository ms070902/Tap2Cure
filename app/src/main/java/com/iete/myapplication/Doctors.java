package com.iete.myapplication;

public class Doctors {
    String Name;
    String Search;
    public Doctors(){}

    public Doctors(String Name) {
        Name = Name;
    }

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        Name = Name;
    }

    public String getSearch() {
        return Search;
    }
    public void setSearch(String Search) {
        Search = Search;
    }
}
